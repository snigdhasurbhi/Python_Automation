package automation1;

public class CombinationOfLiterals {

	public static void main(String[] args) {
		// Ascii value of 0 is 48
		int value='0';
		
		//Ascii value of 7 is 55
		int valu='7';
		
		System.out.println("Greek!!"+ 0 + "45" + 7);
		
		System.out.println("Greek!!"+ value + "45" + valu);
	}

}

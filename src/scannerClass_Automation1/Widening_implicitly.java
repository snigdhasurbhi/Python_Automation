package scannerClass_Automation1;

public class Widening_implicitly {

	public static void main(String[] args) {
		// implicitly
		int X = 70;
		float z = X;
		double y = z;
		
		
		System.out.println(" Print the value for X" + X);
		System.out.println(" Print the value for Z" + z);
		System.out.println(" Print the value for Y" + y);
	}

}
